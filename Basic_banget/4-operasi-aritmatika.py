# Operasi aritmatika

a = 15
b = 7

# Operasi tambah
hasil = a + b
print(f"s + b = {hasil}")

# Operasi pengurangan
hasil = a - b
print(f"s - b = {hasil}")

# Operasi peerkalian
hasil = a * b
print(f"s * b = {hasil}")

# Operasi peembagian
hasil = a / b
print(f"s / b = {hasil}")

# Operasi modulus
hasil = a % b
print(f"s % b = {hasil}")

# Operasi floor division
hasil = a // b
print(f"s // b = {hasil}")

# Prioritas operasi
x = 4
y = 2
z = 1

hasil = x ** y * z + x / y - y %z // x
print(f"hasil: {hasil}")